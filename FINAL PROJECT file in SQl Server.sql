CREATE DATABASE BUS_RESERVATION_SYSTEM
USE BUS_RESERVATION_SYSTEM
CREATE TABLE USERS(USERS_ID INT IDENTITY (1,1) PRIMARY KEY,FNAME VARCHAR(20) NOT NULL,LNAME VARCHAR(20) NOT NULL,ADDRE VARCHAR(40) NOT NULL,PHONE VARCHAR(20) NOT NULL,CNIC VARCHAR(30) NOT NULL,EMAIL VARCHAR(30) NOT NULL);
CREATE TABLE LOGINS(LOGIN_ID INT IDENTITY (1,1), LOGIN_ROLE_ID INT,LOGIN_USERNAME VARCHAR(30)NOT NULL,USER_PASSWARD VARCHAR(50)NOT NULL,USERS_ID INT );
CREATE TABLE PASSENGER(PASS_ID INT IDENTITY(1,1),PASS_FNAME VARCHAR(30),PASS_LNAME VARCHAR(30),PASS_ADD VARCHAR(40),PASS_MOBILE VARCHAR(30),PASS_CNIC VARCHAR(40),PASS_EMAIL VARCHAR(50),USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID));
CREATE TABLE TICKET_BOOKING(TICKET_ID INT IDENTITY(1,1),TICKET_TITLE VARCHAR(30) DEFAULT 'NIAZI EXPRESS',TICKET_SUB_TITLE VARCHAR(30) DEFAULT 'LAHORE TO KARACHI',TICKET_TYPE VARCHAR(30) DEFAULT 'VIP',DEPARTURE_DATE_TIME VARCHAR(40) DEFAULT '1-JULY-2022,7:00AM',
DESTINATION_DATE_TIME VARCHAR(40)DEFAULT '1-JULY-2022,12:00AM',TICKET_PRICE INT NOT NULL,TICKET_DESC VARCHAR(30) DEFAULT '10%',USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),PASS_ID INT );
CREATE TABLE PAYMENT(PAYMENT_ID INT IDENTITY(1,1),PAYMENT_METHOD VARCHAR(40)DEFAULT 'CASH',PAYMENT_DATE VARCHAR(20)DEFAULT '1-JULY-2022,7:00AM',USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),
PASS_ID INT FOREIGN KEY REFERENCES PASSENGER(PASS_ID),TICKET_ID INT );
CREATE TABLE FARE(FARE_ID INT IDENTITY(1,1) ,FARE_TITLE VARCHAR(20)DEFAULT 'REGULAR',FARE_DESC VARCHAR(20) DEFAULT '10%',FARE_TYPE VARCHAR(20)DEFAULT 'VIP',USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),
PASS_ID INT FOREIGN KEY REFERENCES PASSENGER(PASS_ID),TICKET_ID INT FOREIGN KEY REFERENCES TICKET_BOOKING(TICKET_ID));
CREATE TABLE BUSES (BUS_ID INT IDENTITY(1,1) PRIMARY KEY,BUS_PLATE_NO INT,BUS_CONDITION VARCHAR(20) DEFAULT'SAVE',CAPACITY INT DEFAULT'35',BUS_TYPE VARCHAR(20) DEFAULT'AC',BUS_NAME VARCHAR(20)DEFAULT'NIAZI EXPRESS',USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),
PASS_ID INT FOREIGN KEY REFERENCES PASSENGER(PASS_ID),TICKET_ID INT FOREIGN KEY REFERENCES TICKET_BOOKING(TICKET_ID));
CREATE TABLE SEAT(SEAT_ID INT IDENTITY(1,1) PRIMARY KEY,SEAT_NO INT,SEAT_TYPE VARCHAR(20)DEFAULT'AC',SEAT_DESC VARCHAR(20)DEFAULT'NO DISCOUNT IN AC',USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),
PASS_ID INT FOREIGN KEY REFERENCES PASSENGER(PASS_ID) ,TICKET_ID INT FOREIGN KEY REFERENCES TICKET_BOOKING(TICKET_ID),BUS_ID INT);
CREATE TABLE TRAVEL_SCHEDULE(SCHEDULE_ID INT IDENTITY(1,1) PRIMARY KEY,SCHEDULE_DEPARTURE VARCHAR(20)DEFAULT'LAHORE',SCHEDULE_DESTINATION VARCHAR(20)DEFAULT'ISLAMABAD',DEPATURE_DATE_TIME VARCHAR(20)DEFAULT'1-JULY-2022,7:00 AM',
SCHEDULE_PATH VARCHAR(20) DEFAULT'M2',USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),TICKET_ID INT FOREIGN KEY REFERENCES TICKET_BOOKING(TICKET_ID),FARE_ID INT ,BUS_ID INT);
CREATE TABLE DRIVER(DRIVER_ID INT IDENTITY(1,1) PRIMARY KEY,FNAME VARCHAR(20),LNAME VARCHAR(20),ADDRES VARCHAR(40),PHONE_NO VARCHAR(20),CNIC VARCHAR(20), EMAIL VARCHAR(30),USERS_ID INT FOREIGN KEY REFERENCES USERS(USERS_ID),BUS_ID INT FOREIGN KEY REFERENCES BUSES(BUS_ID),
SCHEDULE_ID INT);
